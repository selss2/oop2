public class Matrix6 {
	public static void main(String[] args) {
		int a=0;
		for (int i = 0; i < 5; i++) {
		for (int j = 0; j < 5; j++) {
			a += 5;
			System.out.print(a+"\t");
		}
		System.out.println("");
		}
	}
}